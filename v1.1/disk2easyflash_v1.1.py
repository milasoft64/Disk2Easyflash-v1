# BASIC LISTING BUILDER  (from disk2easyflash.c)
# Builds the synthetic "$" LOAD-and-LIST directory program.
# ===========================================================================

_TXT_PRG         = b'PRG'
_TXT_DEL         = b'DEL'
_TXT_BLOCKS_FREE = b'BLOCKS FREE.'


def build_listing(entries: List[Entry], blocks_free: int = 2) -> Entry:
    """
    Build a C64 BASIC listing (the '$' directory file) and return it as
    an Entry so it can be bundled into the cartridge alongside the real files.
    """
    buf = bytearray()

    # BASIC load address ($0401)
    buf += b'\x01\x04'

    # --- Header line: 0  "DISK NAME"  ID ---
    header = entries[0]
    name_bytes = header.name.encode('latin-1', errors='replace')[:16]
    line = bytearray()
    line += b'\x01\x01'          # next-line pointer (placeholder, C64 fixes it)
    line += b'\x00\x00'          # line number 0
    line.append(0x12)             # REVERSE ON
    line.append(0x22)             # QUOTE "
    line += name_bytes
    line += b'\x20' * (16 - len(name_bytes))  # pad to 16
    line.append(0x22)             # QUOTE "
    line.append(0x20)             # SPACE
    line += header.id.encode('latin-1', errors='replace')[:5]
    line.append(0x00)             # end of line
    buf += bytes(line)

    # --- One line per file entry ---
    for entry in entries[1:]:
        if entry.type == '*':
            continue
        size  = (entry.length + 253) // 254
        ftype = _TXT_DEL if entry.type == 'd' else _TXT_PRG

        line = bytearray()
        line += b'\x01\x01'                    # next-line pointer
        line += bytes([size & 0xFF, size >> 8])  # line number = block count

        # Left-pad size number with spaces so the filename column lines up
        spaces = 3 if size < 10 else (2 if size < 100 else 1)
        line += b'\x20' * spaces

        line.append(0x22)  # "
        fname = entry.name.encode('latin-1', errors='replace')[:16]
        line += fname
        line.append(0x22)  # "
        line += b'\x20' * (17 - len(fname))  # pad so type is always at col 20
        line += ftype
        line.append(0x00)
        buf += bytes(line)

    # --- Last line: N  BLOCKS FREE. ---
    line = bytearray()
    line += b'\x01\x01'
    line += bytes([blocks_free & 0xFF, blocks_free >> 8])
    line += _TXT_BLOCKS_FREE
    line.append(0x00)
    buf += bytes(line)

    # End of BASIC program
    buf += b'\x00\x00'

    return Entry('$', 'p', data=bytes(buf))


