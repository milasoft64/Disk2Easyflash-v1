def _slot_key(n: int) -> str:
    """Python-UI display key for slot *n* (1-based): '1'-'9' then 'A'-'Z'."""
    if n <= 9:
        return str(n)
    return chr(ord('A') + n - 10)   # 10→A, 11→B, …, 35→Z


def _parse_disk_keys(raw: str, total: int) -> List[int]:
    """
    Parse a string of disk keys — tolerates any mix of whitespace, commas,
    semicolons or other punctuation as separators (e.g. '1 3', '1,2,3', '1, 2, A').
    Returns a deduplicated list of 1-based indices in the order entered.
    Accepts digits 1-9 and letters A-Z (case-insensitive).
    """
    result = []
    seen   = set()
    for tok in re.split(r'[\s,;]+', raw.upper().strip()):
        if not tok:
            continue
        if tok.isdigit():
            n = int(tok)
            if 1 <= n <= min(9, total) and n not in seen:
                result.append(n)
                seen.add(n)
        elif len(tok) == 1 and 'A' <= tok <= 'Z':
            n = ord(tok) - ord('A') + 10
            if 1 <= n <= total and n not in seen:
                result.append(n)
                seen.add(n)
    return result


