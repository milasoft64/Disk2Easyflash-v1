# M2I FILE PARSER  (replaces m2i.c)
# ===========================================================================

def parse_m2i(filename: str) -> List[Entry]:
    """
    Parse an M2I mapping file.
    Format per line (after the header):  TYPE:host_filename:C64 name
    Returns a list with header Entry first.
    """
    m2i_dir = os.path.dirname(os.path.abspath(filename))
    entries: List[Entry] = []

    with open(filename, 'r', errors='replace') as f:
        raw = f.read()

    # Normalise line endings
    lines = raw.replace('\r\n', '\n').replace('\r', '\n').split('\n')
    if not lines:
        sys.exit(f"Empty M2I file: {filename}")

    # First non-empty line is the disk name
    disk_name = lines[0][:16].rstrip()
    entries.append(Entry(disk_name, '*', disk_id='EF 2A'))

    for line in lines[1:]:
        line = line.rstrip('\n')
        if not line or line.startswith('#'):
            continue

        # Split on first two colons
        col1 = line.find(':')
        col2 = line.find(':', col1 + 1) if col1 >= 0 else -1
        if col1 < 0 or col2 < 0:
            sys.exit(f"M2I parse error: '{line}'")

        etype     = line[0].lower()
        host_file = line[col1 + 1: col2].rstrip()
        c64_name  = line[col2 + 1:].rstrip()[:16]

        if etype not in ('p', 'd', 'q'):
            sys.exit(f"M2I bad type in: '{line}'")

        if etype in ('p', 'q'):
            host_path = os.path.join(m2i_dir, host_file)
            try:
                with open(host_path, 'rb') as f:
                    data = f.read()
            except IOError:
                sys.exit(f"Cannot open M2I file: '{host_path}'")
            entries.append(Entry(c64_name, etype, data=data))
        else:
            entries.append(Entry(c64_name, 'd'))

    return entries


