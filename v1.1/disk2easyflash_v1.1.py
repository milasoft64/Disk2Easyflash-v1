def _safe_excepthook(exc_type, exc_val, exc_tb):
    """
    Replace the default traceback dump so internal file paths and line numbers
    are never shown to end users.  KeyboardInterrupt prints a clean message;
    all other exceptions print only their human-readable text.
    Set DISK2EF_DEBUG=1 in your environment to restore full tracebacks.
    """
    if exc_type is KeyboardInterrupt:
        print('\n\n  Cancelled.')
    else:
        print(f'\n  Error: {exc_val}', file=sys.stderr)
        print('  (Run with DISK2EF_DEBUG=1 for full traceback)', file=sys.stderr)


def main():
    # Install safe excepthook so no internal paths/line numbers leak to users.
    # Bypass with:  set DISK2EF_DEBUG=1   (Windows)
    #               export DISK2EF_DEBUG=1  (Linux/macOS)
    if not os.environ.get('DISK2EF_DEBUG'):
        sys.excepthook = _safe_excepthook

    args_raw = sys.argv[1:]

    # ── Interactive menu when no arguments are given ─────────────────────────
    if not args_raw:
        try:
            run_menu()
        except KeyboardInterrupt:
            print('\n\n  Cancelled.')
            try:
                _pause()
            except KeyboardInterrupt:
                pass
        return

    # ── CLI mode — guard against Ctrl+C and unexpected crashes ───────────────
    try:
        _cli(args_raw)
    except KeyboardInterrupt:
        print('\n\n  Cancelled.', file=sys.stderr)
        sys.exit(1)
    except Exception as ex:
        print(f'\n  Error: {ex}', file=sys.stderr)
        if os.environ.get('DISK2EF_DEBUG'):
            raise
        sys.exit(1)


def _cli(args_raw):
    """All CLI logic, separated so main() can wrap it cleanly."""

    if '--multi' in args_raw:
        rest = [a for a in args_raw if a != '--multi']
        if len(rest) < 2:
            sys.exit('Usage: disk2easyflash --multi <disk1.d64> [disk2.d64 ...] <output.crt>')
        output_crt = rest[-1]   # output is always last
        disk_files  = rest[:-1]
        for df in disk_files:
            if not os.path.isfile(df):
                sys.exit(f'File not found: {df}')
        if len(disk_files) < 2:
            sys.exit('--multi needs at least 2 disk images')
        print(f"disk2easyflash: multi-disk mode, {len(disk_files)} images -> {output_crt}", file=sys.stderr)
        build_multi_crt(disk_files, output_crt)
        return

    non_flag  = [a for a in args_raw if not a.startswith('-')]
    disk_args = [a for a in non_flag if any(a.lower().endswith(e) for e in _DISK_EXTS)]
    crt_args  = [a for a in non_flag if a.lower().endswith('.crt')]
    if len(disk_args) >= 2 and len(crt_args) == 1:
        output_crt = crt_args[0]
        disk_files  = disk_args
        for df in disk_files:
            if not os.path.isfile(df):
                sys.exit(f'File not found: {df}')
        print(f"disk2easyflash: multi-disk mode, {len(disk_files)} images -> {output_crt}", file=sys.stderr)
        build_multi_crt(disk_files, output_crt)
        return

    mode_flags = {'--crt', '-c', '--xbank', '-x', '--list'}
    if not any(a in mode_flags for a in args_raw):
        if len(disk_args) == 1 and len(crt_args) == 1:
            # Inject --crt into our local copy only — never mutate sys.argv
            args_raw = ['--crt'] + list(args_raw)
            print("disk2easyflash: single-disk mode (auto-detected --crt)", file=sys.stderr)

    p = argparse.ArgumentParser(
        prog='disk2easyflash',
        description='*** DISK2EASYFLASH V1.00 ***  Convert C64 disk images to EasyFlash cartridges.',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            'Examples:\n'
            '  %(prog)s --crt game.d64 game.crt\n'
            '  %(prog)s --crt game.d81 game.crt\n'
            '  %(prog)s --xbank --low game.d64 game.crt\n'
            '  %(prog)s --list game.d81\n'
            '  %(prog)s --crt --remove 3 --ignore 5 game.d64 game.crt\n'
            '  %(prog)s --multi pacman.d64 rambo.d81 out.crt\n'
        ),
    )

    build = p.add_mutually_exclusive_group(required=True)
    build.add_argument('--crt',   '-c', action='store_true',
                       help='Build an EasyFlash cartridge (.crt)')
    build.add_argument('--xbank', '-x', action='store_true',
                       help='Build an EasyFlash XBANK cartridge')
    build.add_argument('--list',  action='store_true',
                       help='List contents of disk image without building')

    bank = p.add_mutually_exclusive_group()
    bank.add_argument('--normal', action='store_true',
                      help='Use full 16 KiB banks (default)')
    bank.add_argument('--low',    action='store_true',
                      help='Use only lower 8 KiB banks ($8000)')
    bank.add_argument('--high',   action='store_true',
                      help='Use only upper 8 KiB banks ($A000, Ultimax)')

    p.add_argument('--nolisting', '-n', action='store_true',
                   help='Do not add the LOAD "$" listing')
    p.add_argument('--blocks-free', type=int, default=2, metavar='N',
                   help='Blocks-free count shown in listing (default: 2)')
    p.add_argument('--remove', '-r', type=int, action='append', default=[],
                   metavar='ID',
                   help='Remove entry #ID from listing and cartridge')
    p.add_argument('--ignore', '-i', type=int, action='append', default=[],
                   metavar='ID',
                   help='Keep entry #ID in listing but not in flash '
                        '(useful for savegame placeholders)')
    p.add_argument('--verbose', '-v', action='store_true',
                   help='Print file listing even in --crt/--xbank mode')

    p.add_argument('input',  help='Input .d64, .d81, or .m2i file (or directory containing .m2i)')
    p.add_argument('output', nargs='?', help='Output .crt file')

    args = p.parse_args(args_raw)

    if not args.list and args.output is None:
        p.error('output file is required for --crt and --xbank')

    # --- Determine bank mode ---
    if args.high:
        mode = 'hi'
    elif args.low:
        mode = 'lo'
    else:
        mode = 'nm'

    # --- Resolve input file ---
    input_path = args.input
    is_m2i     = False

    if os.path.isdir(input_path):
        found = next(
            (os.path.join(input_path, f)
             for f in os.listdir(input_path)
             if f.lower().endswith('.m2i')),
            None,
        )
        if found is None:
            sys.exit(f"No .m2i file found in directory: {input_path}")
        input_path = found
        is_m2i     = True
    else:
        is_m2i = input_path.lower().endswith('.m2i')

    # --- Parse disk image (D64, D81, or M2I) ---
    entries = _load_disk(input_path)

    # --- Apply --remove ---
    for remove_id in args.remove:
        num = 0
        done = False
        for e in entries[1:]:
            if e.type != '*':
                num += 1
                if num == remove_id:
                    e.type = '*'
                    done = True
                    break
        if not done:
            sys.exit(f"Cannot remove entry {remove_id}: no such entry")

    # --- Apply --ignore ---
    for ignore_id in args.ignore:
        num = 0
        done = False
        for e in entries[1:]:
            if e.type == 'p':
                num += 1
                if num == ignore_id:
                    e.type = 'q'
                    done = True
                    break
        if not done:
            sys.exit(f"Cannot ignore entry {ignore_id}: no such entry")

    # --- Build listing ---
    if not args.nolisting:
        listing = build_listing(entries, args.blocks_free)
        entries.append(listing)

    # --- Print listing (--list or --verbose) ---
    if args.list or args.verbose:
        hdr = entries[0]
        print(f'      | 0 "{hdr.name:<16}" {hdr.id} |')
        num = 0
        for idx, e in enumerate(entries[1:], 1):
            if e.type == '*':
                continue
            if args.list and not args.nolisting and idx == len(entries) - 1:
                break
            num += 1
            size   = (e.length + 253) // 254
            qname  = f'"{e.name}"'
            ftype  = 'DEL' if e.type == 'd' else 'PRG'
            note   = ', not in flash, only in listing' if e.type == 'q' else ''
            print(f'{num:4d}  | {size:<4d}  {qname:<18} {ftype} |  '
                  f'{e.length:5d} Bytes{note}')

        if not args.list and not args.nolisting and len(entries) > 1:
            last = entries[-1]
            print(f'      |       The Listing ("$")      |  {last.length:5d} Bytes')

    # --- Build output file ---
    if not args.list:
        basename = os.path.basename(input_path)
        with open(args.output, 'wb') as out:
            if args.crt:
                write_cart_header(out, CART_TYPE_EASYFLASH, 1, 0, basename)
                write_bank0_crt(out, mode)
                if mode == 'nm':
                    bundle(out, entries, 0x4000, 0x8000, 14, KAPI_NM, None, 1)
                elif mode == 'lo':
                    bundle(out, entries, 0x2000, 0x8000, 13, KAPI_LO, None, 1)
                else:  # hi
                    bundle(out, entries, 0x2000, 0xA000, 13, KAPI_HI, LAUNCHER_HI, 1)

            else:  # --xbank
                if mode == 'hi':
                    exrom, game = 1, 0
                elif mode == 'lo':
                    exrom, game = 0, 1
                else:
                    exrom, game = 0, 0
                write_cart_header(out, CART_TYPE_EASYFLASH_XBANK, exrom, game, basename)
                if mode == 'nm':
                    bundle(out, entries, 0x4000, 0x8000, 14, KAPI_NM, None, 0)
                elif mode == 'lo':
                    bundle(out, entries, 0x2000, 0x8000, 13, KAPI_LO, None, 0)
                else:  # hi
                    bundle(out, entries, 0x2000, 0xA000, 13, KAPI_HI, LAUNCHER_HI, 0)

        print(f"Written: {args.output}", file=sys.stderr)


