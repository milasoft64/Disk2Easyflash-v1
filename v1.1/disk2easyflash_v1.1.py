# D81 PARSER  (1581, 80-track, 3200-block MFM format)
#
# Layout differences from D64:
#   • 80 tracks × 40 sectors × 256 bytes  =  819 200 bytes
#   • BAM:  Track 40, Sector 1  (first BAM sector; sector 2 is the second half)
#           Disk name at BAM1 offset 0x04, 16 bytes
#           Disk ID   at BAM1 offset 0x16, 5 bytes  (2-char + 0xA0 + 2-char)
#   • Directory starts at Track 40, Sector 3
#   • Directory sectors: first two bytes are next-track / next-sector as usual,
#     but track 0 / sector 255 means "last sector" (all 8 entries valid up to
#     the byte count stored in sector[1] when track==0).
#     In practice the 1581 uses the same chain-termination convention as the
#     1541 (track=0, sector=last_used_byte), so the generic chain reader works.
#   • Directory entries are 32 bytes each, 8 per sector, starting at byte 0
#     (NOT byte 2 as in D64).  The first sector starts at byte 2 because bytes
#     0–1 are the forward-link; subsequent sectors also start at byte 2.
#     → We read consistently from byte 2 of every directory sector, same as D64.
#   • File type byte is at slot offset 0 (same as D64).
#   • Start track/sector at slot offsets 1 and 2 (same as D64).
#   • File name at slot offsets 3–18 (same as D64).
# ===========================================================================

# Standard D81 image size: 80 tracks × 40 sectors × 256 bytes
D81_SIZE         = 819_200
D81_BAM_TRACK    = 40
D81_BAM_SECTOR   = 1          # first BAM sector (sector 2 is second half)
D81_DIR_TRACK    = 40
D81_DIR_SECTOR   = 3

# Offsets within the first BAM sector
D81_BAM_NAME_OFF = 0x04       # disk name, 16 bytes
D81_BAM_ID_OFF   = 0x16       # disk ID,   5 bytes  (aa 0xA0 bb cc format)

def parse_d81(filename: str) -> List[Entry]:
    """
    Parse a D81 disk image (1581, 80-track, 3200-block MFM format).
    Returns a list whose first element is the disk header Entry (type='*')
    followed by one Entry per directory slot.
    """
    with open(filename, 'rb') as f:
        img = f.read()

    if len(img) != D81_SIZE:
        print(
            f"Warning: {filename} is {len(img)} bytes "
            f"(expected {D81_SIZE} for a standard D81); continuing anyway.",
            file=sys.stderr,
        )

    entries: List[Entry] = []

    # --- Disk header: BAM at Track 40, Sector 1 ---
    bam = _read_sector(img, D81_BAM_TRACK, D81_BAM_SECTOR, get_d81_offset)
    disk_name = _petscii_name(bam[D81_BAM_NAME_OFF: D81_BAM_NAME_OFF + 16])
    disk_id   = _petscii_name(bam[D81_BAM_ID_OFF:   D81_BAM_ID_OFF + 5])
    entries.append(Entry(disk_name, '*', disk_id=disk_id))

    # --- Directory: starts at Track 40, Sector 3 ---
    dir_track, dir_sector = D81_DIR_TRACK, D81_DIR_SECTOR
    visited_dir: set = set()
    while dir_track != 0:
        if (dir_track, dir_sector) in visited_dir:
            print(
                f"Warning: circular directory chain at track {dir_track} "
                f"sector {dir_sector} -- truncating",
                file=sys.stderr,
            )
            break
        if not (1 <= dir_track <= 80):
            print(
                f"Warning: invalid directory link to track {dir_track} "
                f"sector {dir_sector} -- stopping",
                file=sys.stderr,
            )
            break
        visited_dir.add((dir_track, dir_sector))
        sec = _read_sector(img, dir_track, dir_sector, get_d81_offset)
        dir_track  = sec[0]
        dir_sector = sec[1]

        # 8 directory entries per sector, 32 bytes each, beginning at byte 2
        for i in range(8):
            base = 2 + i * 32
            slot = sec[base: base + 32]

            file_type_raw = slot[0]
            if not (file_type_raw & 0x80):
                continue  # empty, scratched, or not properly closed

            ft_id = file_type_raw & 0x07
            if ft_id == 0:
                ft = 'd'   # DEL
            elif ft_id == 2:
                ft = 'p'   # PRG
            else:
                print(f"Warning: unsupported CBM file type {ft_id} — skipping",
                      file=sys.stderr)
                continue

            file_track  = slot[1]
            file_sector = slot[2]
            name        = _petscii_name(slot[3: 3 + 16])

            if ft == 'd':
                entries.append(Entry(name, 'd'))
            else:
                try:
                    file_data = _read_file_chain(img, file_track, file_sector,
                                                 get_d81_offset)
                    entries.append(Entry(name, 'p', data=file_data))
                except Exception as ex:
                    print(f"Warning: cannot read '{name}': {ex}", file=sys.stderr)

    return entries

