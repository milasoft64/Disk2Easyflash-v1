def _enable_ansi():
    """Enable ANSI escape codes on Windows 10+; no-op on Linux/macOS."""
    if sys.platform == 'win32':
        try:
            import ctypes
            kernel32 = ctypes.windll.kernel32          # type: ignore[attr-defined]
            # Enable ENABLE_VIRTUAL_TERMINAL_PROCESSING (0x0004) on stdout
            handle = kernel32.GetStdHandle(-11)        # STD_OUTPUT_HANDLE
            mode   = ctypes.c_ulong(0)
            if kernel32.GetConsoleMode(handle, ctypes.byref(mode)):
                kernel32.SetConsoleMode(handle, mode.value | 0x0004)
        except Exception:
            pass  # graceful fallback — text just won't be coloured

_COL_WHITE = '\033[97m'   # bright white
_COL_RESET = '\033[0m'

def _w(text: str) -> str:
    """Wrap text in bright-white ANSI colour."""
    return f'{_COL_WHITE}{text}{_COL_RESET}'

