class Entry:
    """
    Represents one entry from a disk image or M2I file.
    type: '*' = disk header, 'p' = PRG, 'd' = DEL, 'q' = listing-only PRG
    """
    __slots__ = ('name', 'id', 'type', 'data', 'length')

    def __init__(self, name: str, etype: str,
                 data: bytes = b'', disk_id: str = ''):
        self.name   = name[:16]
        self.id     = disk_id[:6]
        self.type   = etype
        self.data   = data
        self.length = len(data)


# ===========================================================================
# SECTOR OFFSET HELPERS
# ===========================================================================

def get_d64_offset(track: int, sector: int) -> int:
    """1541 (D64) byte offset for a given track/sector."""
    if track <= 17:
        offset = (track - 1) * 21
    elif track <= 24:
        offset = 17 * 21 + (track - 18) * 19
    elif track <= 30:
        offset = 17 * 21 + 7 * 19 + (track - 25) * 18
    else:
        offset = 17 * 21 + 7 * 19 + 6 * 18 + (track - 31) * 17
    return (offset + sector) * 256

# Alias used internally so both names resolve correctly.
_d64_sector_offset = get_d64_offset

def get_d81_offset(track: int, sector: int) -> int:
    """1581 (D81) byte offset: 80 tracks × 40 sectors × 256 bytes."""
    return ((track - 1) * 40 + sector) * 256

# ===========================================================================
# D64 / D81 LOW-LEVEL HELPERS  (shared by parse_d64 and parse_d81)
# ===========================================================================

def _read_sector(img: bytes, track: int, sector: int,
                 offset_fn=None) -> bytes:
    """Return the raw 256-byte sector using the supplied offset function."""
    if offset_fn is None:
        offset_fn = get_d64_offset
    off = offset_fn(track, sector)
    return img[off: off + 256]


def _petscii_name(raw: bytes) -> str:
    """
    Convert a PETSCII filename to a plain Python string.
    Stops at the first 0xA0 padding byte (standard CBM padding) or 0x00
    (used by some D81 formatters instead of 0xA0).
    """
    chars = []
    for b in raw:
        if b == 0xA0 or b == 0x00:
            break
        chars.append(chr(b))
    return ''.join(chars)


def _petscii_to_screen(name: str, max_len: int = 0) -> bytes:
    """
    Convert a PETSCII disk-name string (from _petscii_name) to C64 screen
    codes.  Handles uppercase PETSCII (0x41–0x5A) AND the shifted/lowercase
    range (0x61–0x7A, 0xC1–0xDA) that some 1581 formatters write, so the
    boot banner always shows the correct disk name regardless of which tools
    created the image.

    If max_len > 0 the result is padded with spaces to exactly max_len bytes
    (used by the multi-disk menu builder).
    """
    src = name.upper()[:max_len] if max_len else name.upper()
    length = max_len if max_len else len(src)
    out = bytearray(length)
    for i, ch in enumerate(src):
        b = ord(ch)
        if 0x41 <= b <= 0x5A:         # A-Z (ASCII / upper PETSCII)
            out[i] = b - 0x40         # → screen codes 1-26
        elif 0xC1 <= b <= 0xDA:       # PETSCII shifted letters after upper()
            out[i] = b - 0xC0
        elif b == 0x20:
            out[i] = 0x20             # space
        elif 0x30 <= b <= 0x39:
            out[i] = b                # digits same in screen codes
        elif b == 0x2D:
            out[i] = 0x2D             # hyphen
        else:
            out[i] = 0x2E             # dot fallback
    return bytes(out)


def _read_file_chain(img: bytes, start_track: int, start_sector: int,
                     offset_fn=None) -> bytes:
    """Follow a CBM sector chain and return the concatenated file data.

    A visited-set guards against corrupt images with circular sector chains;
    if a cycle is detected the chain is truncated and a warning is printed.
    """
    if offset_fn is None:
        offset_fn = get_d64_offset
    data    = bytearray()
    track, sector = start_track, start_sector
    visited: set  = set()
    while track != 0:
        if (track, sector) in visited:
            print(
                f"Warning: circular sector chain at track {track} sector {sector}"
                f" -- truncating",
                file=sys.stderr,
            )
            break
        visited.add((track, sector))
        sec         = _read_sector(img, track, sector, offset_fn)
        next_track  = sec[0]
        next_sector = sec[1]
        if next_track == 0:
            data.extend(sec[2: next_sector + 1])
        else:
            data.extend(sec[2:256])
        track  = next_track
        sector = next_sector
    return bytes(data)


