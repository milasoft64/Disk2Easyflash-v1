def _find_disk_images(root: str = '.') -> list:
    """
    Recursively scan *root* for disk image files (.d64 / .d81 / .m2i).
    Returns a sorted list of absolute paths.
    """
    found = []
    for dirpath, _dirs, files in os.walk(root):
        for fname in sorted(files):
            if any(fname.lower().endswith(ext) for ext in _DISK_EXTS):
                found.append(os.path.abspath(os.path.join(dirpath, fname)))
    return sorted(found)


def _short(path: str) -> str:
    """Return just the filename for display; keep full path internally."""
    return os.path.basename(path)


def _prompt(msg: str, default: str = '') -> str:
    """Show a prompt, return stripped input (or default on empty)."""
    suffix = f' [{default}]' if default else ''
    val = input(f'{_COL_WHITE}  {msg}{suffix}: {_COL_RESET}').strip()
    return val if val else default


def _choose(msg: str, options: list, default: int = 1) -> int:
    """Show numbered options; return 1-based index of chosen item."""
    print()
    for i, opt in enumerate(options, 1):
        marker = '>' if i == default else ' '
        print(_w(f'  {marker} {i}. {opt}'))
    print()
    while True:
        raw = _prompt(msg, str(default))
        try:
            n = int(raw)
            if 1 <= n <= len(options):
                return n
        except ValueError:
            pass
        print(_w(f'  Please enter a number between 1 and {len(options)}.'))


def _yn(msg: str, default: bool = True) -> bool:
    """Ask a yes/no question.  BUG-FIX: actually retries on invalid input."""
    hint = 'Y/n' if default else 'y/N'
    while True:
        raw = _prompt(f'{msg} ({hint})').lower()
        if raw == '':
            return default
        if raw in ('y', 'yes'):
            return True
        if raw in ('n', 'no'):
            return False
        print(_w("  Please answer 'y' or 'n'."))


def _pause():
    """Wait for the user to press Enter — useful at the end when run as an EXE."""
    input('\n  Press Enter to exit...')


