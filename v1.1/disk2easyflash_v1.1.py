# D64 PARSER  (1541, 35-track, 683 blocks)
# ===========================================================================

def parse_d64(filename: str) -> List[Entry]:
    """
    Parse a D64 disk image (MFM, 1541 format).
    Returns a list whose first element is the disk header Entry (type='*')
    followed by one Entry per directory slot.
    """
    with open(filename, 'rb') as f:
        img = f.read()

    # Validate: standard D64 = 174848 bytes; with error bytes = 175531
    if len(img) not in (174848, 175531):
        print(
            f"Warning: {filename} is {len(img)} bytes "
            f"(expected 174848 for a standard D64); continuing anyway.",
            file=sys.stderr,
        )

    entries: List[Entry] = []

    # --- Disk header: track 18, sector 0 ---
    bam = _read_sector(img, 18, 0, get_d64_offset)
    disk_name = _petscii_name(bam[0x90: 0x90 + 16])   # offset 144
    disk_id   = _petscii_name(bam[0xA2: 0xA2 + 5])    # offset 162
    entries.append(Entry(disk_name, '*', disk_id=disk_id))

    # --- Directory: starts at track 18, sector 1 ---
    dir_track, dir_sector = 18, 1
    visited_dir: set = set()
    while dir_track != 0:
        if (dir_track, dir_sector) in visited_dir:
            print(
                f"Warning: circular directory chain at track {dir_track} "
                f"sector {dir_sector} -- truncating",
                file=sys.stderr,
            )
            break
        if not (1 <= dir_track <= 35):
            print(
                f"Warning: invalid directory link to track {dir_track} "
                f"sector {dir_sector} -- stopping",
                file=sys.stderr,
            )
            break
        visited_dir.add((dir_track, dir_sector))
        sec = _read_sector(img, dir_track, dir_sector, get_d64_offset)
        dir_track  = sec[0]
        dir_sector = sec[1]

        # 8 directory entries per sector, 32 bytes each, beginning at byte 2
        for i in range(8):
            base = 2 + i * 32
            slot = sec[base: base + 32]

            file_type_raw = slot[0]
            if not (file_type_raw & 0x80):
                continue  # empty, scratched, or not properly closed

            ft_id = file_type_raw & 0x07
            if ft_id == 0:
                ft = 'd'   # DEL
            elif ft_id == 2:
                ft = 'p'   # PRG
            else:
                print(f"Warning: unsupported CBM file type {ft_id} — skipping",
                      file=sys.stderr)
                continue

            file_track  = slot[1]
            file_sector = slot[2]
            name        = _petscii_name(slot[3: 3 + 16])

            if ft == 'd':
                entries.append(Entry(name, 'd'))
            else:
                try:
                    file_data = _read_file_chain(img, file_track, file_sector,
                                                 get_d64_offset)
                    entries.append(Entry(name, 'p', data=file_data))
                except Exception as ex:
                    print(f"Warning: cannot read '{name}': {ex}", file=sys.stderr)

    return entries


