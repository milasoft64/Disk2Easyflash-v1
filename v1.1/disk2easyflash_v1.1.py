def _disk_key(i: int) -> str:
    """Return the keyboard shortcut for disk slot i (0-based): '1'-'9' then 'A'-'Z'."""
    return str(i + 1) if i < 9 else chr(ord('A') + i - 9)


def _build_menu_prg(disk_names: list, bank_numbers: list) -> bytes:
    """
    Build a C64 PRG (load address $080D) containing a boot menu.
    Accepts keys 1-9 for the first nine disks, then A-Z for disks 10-35.
    Cleanly hand-assembled 6502 with a two-pass label system.
    """
    n = len(disk_names)
    if not (1 <= n <= 35):
        raise ValueError(f"menu supports 1–35 disks, got {n}")

    LOAD   = 0x080D
    CHROUT = 0xFFD2
    GETIN  = 0xFFE4
    SCNCLR = 0xE544

    code   = bytearray()
    labels = {}
    fixups = []

    def here():
        return LOAD + len(code)

    def emit(*bs):
        code.extend(bs)

    def label(name):
        labels[name] = here()

    def ref_abs(name):
        fixups.append((len(code), name, 'abs16'))
        emit(0x00, 0x00)

    def ref_lo(name):
        fixups.append((len(code), name, 'lo'))
        emit(0x00)

    def ref_hi(name):
        fixups.append((len(code), name, 'hi'))
        emit(0x00)

    def ref_rel(name):
        fixups.append((len(code), name, 'rel8'))
        emit(0x00)

    def patch():
        for off, name, kind in fixups:
            addr = labels[name]
            if kind == 'abs16':
                code[off]   = addr & 0xFF
                code[off+1] = (addr >> 8) & 0xFF
            elif kind == 'lo':
                code[off] = addr & 0xFF
            elif kind == 'hi':
                code[off] = (addr >> 8) & 0xFF
            elif kind == 'rel8':
                delta = addr - (LOAD + off + 1)
                if not (-128 <= delta <= 127):
                    raise AssertionError(
                        f"Branch to '{name}' out of rel8 range ({delta}); "
                        "too many disk entries?"
                    )
                code[off] = delta & 0xFF

    strings     = bytearray()
    string_lbls = {}

    def defstr(name, data: bytes):
        string_lbls[name] = len(strings)
        strings.extend(data)
        strings.append(0x00)

    def jsr_print(str_label):
        emit(0xA9); ref_lo(str_label)
        emit(0x85, 0xFB)
        emit(0xA9); ref_hi(str_label)
        emit(0x85, 0xFC)
        emit(0x20); ref_abs('print_str')

    defstr('s_header', bytes([0x0D, 0x12]) + b'  *** DISK2EASYFLASH V1.1 ***  ' + bytes([0x92, 0x0D]))
    defstr('s_crd', bytes([0x20, 0x20, 0x20, 0x4D, 0x49, 0x4C, 0x41, 0x53, 0x4F, 0x46, 0x54, 0x36, 0x34, 0x2E, 0x49, 0x54, 0x43, 0x48, 0x2E, 0x49, 0x4F, 0x0D, 0x0D]))
    defstr('s_select', b'  SELECT A DISK:\r\r')
    for i, name in enumerate(disk_names):
        trunc = name.upper()[:16]
        key   = _disk_key(i)          # '1'-'9' then 'A'-'Z'
        line  = ('  ' + key + '. ' + trunc + '\r').encode('latin-1', errors='replace')
        defstr('s_disk' + str(i), line)

    # ── Preamble ─────────────────────────────────────────────────────────────
    emit(0x78)                         # SEI
    emit(0x20, SCNCLR&0xFF, SCNCLR>>8) # JSR SCNCLR
    emit(0x58)                         # CLI
    emit(0xA9, 0x00)
    emit(0x8D, 0x20, 0xD0)             # STA $D020 (border colour = black)
    emit(0x8D, 0x21, 0xD0)             # STA $D021 (bg colour = black)
    emit(0x85, 0xC6)                   # STA $C6   (198) clear keyboard buffer
    emit(0xA9, 0x01)
    emit(0x8D, 0x86, 0x02)             # STA $0286 (KERNAL char colour = white)

    emit(0x4C); ref_abs('main')        # JMP main

    # ── print_str: print NUL-terminated string pointed to by $FB/$FC ─────────
    label('print_str')
    emit(0xA0, 0x00)                   # LDY #0
    label('ps_loop')
    emit(0xB1, 0xFB)                   # LDA ($FB),Y
    emit(0xF0); ref_rel('ps_done')     # BEQ ps_done
    emit(0x20, CHROUT&0xFF, CHROUT>>8) # JSR CHROUT
    emit(0xC8)                         # INY
    emit(0xD0); ref_rel('ps_loop')     # BNE ps_loop
    emit(0xE6, 0xFC)                   # INC $FC  (high byte of pointer)
    emit(0xD0); ref_rel('ps_loop')     # BNE ps_loop
    label('ps_done')
    emit(0x60)                         # RTS

    # ── main: disk list ────────────────────────
    label('main')
    jsr_print('s_header')
    jsr_print('s_crd')
    jsr_print('s_select')
    for i in range(n):
        jsr_print(f's_disk{i}')

    # ── wait_key: accept '1'-'9' (disks 1-9) and 'A'-'Z'/'a'-'z' (10-35) ───
    #
    # Key → 0-based index mapping:
    #   '1'..'9'  (0x31..0x39)  →  index 0..8
    #   'A'..'Z'  (0x41..0x5A)  →  index 9..34
    #   'a'..'z'  (0x61..0x7A)  →  index 9..34  (same as uppercase)
    #
    label('wait_key')
    emit(0x20, GETIN&0xFF, GETIN>>8)  # JSR GETIN  → key in A

    # --- digits '1'-'9' ---
    emit(0xC9, 0x31)                   # CMP #'1'
    emit(0x90); ref_rel('wait_key')    # BCC wait_key  (< '1')
    emit(0xC9, 0x3A)                   # CMP #'9'+1
    emit(0xB0); ref_rel('try_upper')   # BCS try_upper (> '9')
    emit(0x38)                         # SEC
    emit(0xE9, 0x31)                   # SBC #'1'   →  A = 0..8
    emit(0x4C); ref_abs('got_index')   # JMP got_index

    # --- uppercase A-Z ---
    label('try_upper')
    emit(0xC9, 0x41)                   # CMP #'A'
    emit(0x90); ref_rel('wait_key')    # BCC wait_key  (gap between ':' and 'A')
    emit(0xC9, 0x5B)                   # CMP #'Z'+1
    emit(0xB0); ref_rel('try_lower')   # BCS try_lower (> 'Z')
    emit(0x38)                         # SEC
    emit(0xE9, 0x41 - 9)               # SBC #0x38   →  A = 9..34  ('A'=9)
    emit(0x4C); ref_abs('got_index')   # JMP got_index

    # --- lowercase a-z ---
    label('try_lower')
    emit(0xC9, 0x61)                   # CMP #'a'
    emit(0x90); ref_rel('wait_key')    # BCC wait_key
    emit(0xC9, 0x7B)                   # CMP #'z'+1
    emit(0xB0); ref_rel('wait_key')    # BCS wait_key
    emit(0x38)                         # SEC
    emit(0xE9, 0x61 - 9)               # SBC #0x58   →  A = 9..34  ('a'=9)
    # fall through to got_index

    # --- got_index: A = 0-based index; validate, then jump to chosen disk ----
    label('got_index')
    emit(0xC9, n)                      # CMP #n
    emit(0xB0); ref_rel('wait_key')    # BCS wait_key  (index >= n → invalid key)
    emit(0xAA)                         # TAX
    emit(0xBD); ref_abs('bank_tbl')    # LDA bank_tbl,X
    emit(0x8D, 0x00, 0xDE)             # STA $DE00  (EasyFlash bank select)
    emit(0x8D, 0x00, 0xDF)             # STA $DF00
    emit(0xA9, 0x07)                   # LDA #$07   (EasyFlash NM mode)
    emit(0x8D, 0x02, 0xDE)             # STA $DE02
    emit(0x4C, 0x00, 0x80)             # JMP $8000  (start chosen disk's KAPI)

    # ── Append string pool and bank table, resolve all fixups ─────────────────
    strings_base = here()
    for sname, soff in string_lbls.items():
        labels[sname] = strings_base + soff

    bank_tbl_base = strings_base + len(strings)
    labels['bank_tbl'] = bank_tbl_base

    patch()

    code.extend(strings)
    for bn in bank_numbers:
        code.append(bn & 0xFF)

    basic_stub = bytes([
        0x0B, 0x08,
        0x0A, 0x00,
        0x9E,
        0x32, 0x30, 0x36, 0x31,
        0x00,
        0x00, 0x00,
    ])

    prg = bytes([0x01, 0x08]) + basic_stub + bytes(code)
    return prg


def _count_chips(data: bytes) -> int:
    """
    Count CHIP packets in raw CRT chip-section bytes by following the
    packet-length field in each header.  Much more reliable than searching
    for the literal bytes b'CHIP' which can appear inside file data.
    """
    count = 0
    pos   = 0
    while pos + 16 <= len(data):
        if data[pos:pos + 4] != b'CHIP':
            break
        pkt_len = struct.unpack_from('>I', data, pos + 4)[0]
        if pkt_len < 16:
            break          # malformed — stop
        count += 1
        pos   += pkt_len
    return count


def build_multi_crt(disk_paths: list, output_path: str, mode: str = 'nm'):
    """
    Build a multi-disk EasyFlash CRT.

    Layout:
      Bank 0  : EasyFlash startup splash (unchanged, jumps to bank 1)
      Bank 1  : Boot menu PRG packed via bundle()
      Banks 2+: One disk image each, packed via bundle()
    """
    if len(disk_paths) > 35:
        sys.exit("multi mode supports at most 35 disk images (keys 1-9, A-Z)")
    if len(disk_paths) < 2:
        sys.exit("multi mode needs at least 2 disk images")

    # Pick bundle parameters for the chosen mode.
    if mode == 'nm':
        _bsize, _boff, _bshift, _api, _launcher = 0x4000, 0x8000, 14, KAPI_NM, None
    elif mode == 'lo':
        _bsize, _boff, _bshift, _api, _launcher = 0x2000, 0x8000, 13, KAPI_LO, None
    else:  # hi
        _bsize, _boff, _bshift, _api, _launcher = 0x2000, 0xA000, 13, KAPI_HI, LAUNCHER_HI

    all_entries = []
    disk_names  = []
    for path in disk_paths:
        entries = parse_m2i(path) if path.lower().endswith('.m2i') else parse_disk(path)
        if not entries[0].name.strip():
            entries[0] = Entry(
                os.path.splitext(os.path.basename(path))[0].upper(),
                entries[0].type, disk_id=entries[0].id)
        all_entries.append(entries)
        disk_names.append(entries[0].name.strip() or os.path.splitext(os.path.basename(path))[0])
        print(f"  Loaded: {path}  ({disk_names[-1]})", file=sys.stderr)

    def dry_run_bundle(entries, first_bank):
        buf = io.BytesIO()
        bundle(buf, entries, _bsize, _boff, _bshift, _api, _launcher, first_bank)
        buf.seek(0)
        data  = buf.read()
        chips = _count_chips(data)
        return data, chips

    disk_start_banks = []
    current_bank = 2
    disk_chip_data = []
    for i, entries in enumerate(all_entries):
        disk_start_banks.append(current_bank)
        chip_data, num_chips = dry_run_bundle(entries, current_bank)
        disk_chip_data.append(chip_data)
        # nm: 2 chips per 16 KB bank; lo/hi: 1 chip per 8 KB bank
        if mode == 'nm':
            banks_used = (num_chips + 1) // 2
        else:
            banks_used = num_chips
        print(f"  Disk {i+1} ({disk_names[i]}): start bank {current_bank}, {banks_used} bank(s)", file=sys.stderr)
        current_bank += banks_used

    if current_bank > MAX_BANKS:
        sys.exit(f"Too many banks needed ({current_bank}); EasyFlash max is {MAX_BANKS}")

    menu_prg = _build_menu_prg(disk_names, disk_start_banks)
    menu_entry = Entry('MENU', 'p', data=menu_prg)
    menu_entries = [Entry('DISK2EASYFLASH', '*', disk_id='EF 2A'), menu_entry]

    with open(output_path, 'wb') as out:
        write_cart_header(out, CART_TYPE_EASYFLASH, 1, 0, 'MULTIDISK')
        write_bank0_crt(out, mode)
        bundle(out, menu_entries, _bsize, _boff, _bshift, _api, _launcher, 1)
        for chip_data in disk_chip_data:
            out.write(chip_data)

    print(f"Written: {output_path}  ({current_bank} banks used)", file=sys.stderr)




