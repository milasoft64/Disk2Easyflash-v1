_DISK_EXTS = ('.d64', '.d81', '.m2i')


def _load_disk(path: str):
    """
    Parse a disk image and ensure entries[0].name is never blank.
    If the BAM disk name is empty (some D81 images leave it blank),
    fall back to the filename stem so the boot banner always shows something.
    """
    entries = parse_m2i(path) if path.lower().endswith('.m2i') else parse_disk(path)
    if not entries[0].name.strip():
        entries[0] = Entry(
            os.path.splitext(os.path.basename(path))[0].upper(),
            entries[0].type,
            disk_id=entries[0].id,
        )
    return entries

