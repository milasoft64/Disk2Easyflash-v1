# Each directory entry in flash: name(16) + bank(2) + offset(2) +
#                                 loadaddr(2) + size(2)  =  24 bytes
_DIR_ENTRY_SIZE = 24


def write_cart_header(out, cart_type: int, exrom: int, game: int, name: str):
    """Write the 64-byte CRT file header."""
    hdr = bytearray(64)
    hdr[0:16]  = CART_SIGNATURE
    struct.pack_into('>I', hdr, 16, 0x40)         # header length = 64
    hdr[20], hdr[21] = 0x01, 0x00                 # version 1.0
    struct.pack_into('>H', hdr, 22, cart_type)
    hdr[24] = exrom
    hdr[25] = game
    # bytes 26-31 reserved (already 0)
    name_b = name.encode('ascii', errors='replace')[:32]
    hdr[32: 32 + len(name_b)] = name_b
    out.write(bytes(hdr))


def _write_chip_header(out, bank_hi: int, bank_lo: int, load_hi: int):
    """Write a single 16-byte CHIP packet header for an 8 KB ROM page."""
    hdr = bytearray(16)
    hdr[0:4]  = CHIP_SIGNATURE
    struct.pack_into('>I', hdr, 4, 0x2010)  # total packet = 16 + 8192
    # hdr[8:10] = chip type 0 (ROM) — already zero
    hdr[10] = bank_hi
    hdr[11] = bank_lo
    hdr[12] = load_hi   # load address high byte: 0x80=$8000 or 0xA0=$A000
    # hdr[13] = 0x00
    hdr[14] = 0x20      # ROM size high byte → 0x2000 = 8 KB
    # hdr[15] = 0x00
    out.write(bytes(hdr))


def write_bank0_crt(out, mode: str):
    """
    Write the startup bank (bank 0) for CRT mode.
    Contains sprites at +$1800 and the boot-splash/startup code at +$1E00.
    Bank 0 occupies only the hi half ($A000) of EasyFlash bank 0.
    """
    buf = bytearray(b'\xff' * 0x2000)
    buf[0x1800: 0x1800 + len(SPRITES)] = SPRITES
    buf[0x1E00: 0x1E00 + len(STARTUP)] = STARTUP

    # Patch startup: bytes 0/1 hold the target bank number and EasyFlash mode
    patch = 0x2000 - len(STARTUP)   # = 0x1E00
    buf[patch + 0] = 1              # jump to bank 1 after splash
    buf[patch + 1] = {'nm': 7, 'lo': 6, 'hi': 5}[mode]

    _write_chip_header(out, bank_hi=0, bank_lo=0, load_hi=0xA0)  # bank 0, $A000
    out.write(bytes(buf))


def bundle(out, entries: List[Entry],
           bank_size: int, bank_offset: int, bank_shift: int,
           api: bytes, launcher: Optional[bytes],
           first_bank: int):
    """
    Pack all PRG files into flash memory and write CHIP packets to `out`.

    bank_size   – bytes per logical bank (0x4000 normal, 0x2000 lo/hi)
    bank_offset – base load address ($8000 or $A000)
    bank_shift  – right-shift to get bank index from byte position (14 or 13)
    api         – compiled 6502 loader (KAPI_NM / KAPI_LO / KAPI_HI)
    launcher    – compiled Ultimax bank-jump launcher, or None
    first_bank  – first CRT bank number to use (0=xbank, 1=crt)
    """
    flash = bytearray(b'\xff' * (MAX_BANKS * 0x4000))
    flash_size = len(flash)
    pos   = 0

    # 1. Write the kernel API (6502 loader code)
    flash[pos: pos + len(api)] = api
    pos += len(api)

    # 2. Write the 40-byte disk-name banner (C64 screen codes, centred)
    # Layout: [padding][****][ ][name][ ][****] -- must fit in 40 bytes.
    # Clamp name to 30 chars; guard center against going negative.
    raw_name  = entries[0].name.rstrip()[:30]
    n         = len(raw_name)
    center    = max(0, (31 - n) // 2)
    banner    = bytearray(b'\x20' * 40)
    scr_name  = _petscii_to_screen(raw_name)   # handles all PETSCII letter ranges
    for k in range(4):
        if center + k < 40:
            banner[center + k] = 0x2A
    for k, sc in enumerate(scr_name):
        idx = center + 5 + k
        if idx < 40:
            banner[idx] = sc
    for k in range(1, 5):
        idx = center + 5 + n + k
        if idx < 40:
            banner[idx] = 0x2A
    flash[pos: pos + 40] = banner
    pos += 40

    # 3. Reserve directory space (count how many PRG entries we have)
    prg_entries = [e for e in entries[1:] if e.type == 'p']
    dir_start   = pos
    pos        += len(prg_entries) * _DIR_ENTRY_SIZE

    # 4. End-of-directory marker
    flash[pos] = 0x00
    pos += 1

    if pos > flash_size:
        sys.exit(
            f"Flash overflow: API + directory headers alone exceed "
            f"{MAX_BANKS} banks ({MAX_BANKS * 0x4000 // 1024} KiB). "
            f"Too many files?"
        )

    # 5. Ultimax launcher at the very end of the first bank (hi mode only)
    if launcher is not None:
        launcher_start = bank_size - len(launcher)
        if pos > launcher_start:
            sys.exit(
                f"Flash overflow: API + directory ({pos} bytes) overlaps the "
                f"Ultimax launcher region at offset {launcher_start:#x}. "
                f"Too many files in hi mode?"
            )
        flash[launcher_start: bank_size] = launcher
        pos = bank_size

    # 6. Write file data and fill in directory entries
    dir_pos = dir_start
    for entry in prg_entries:
        bank   = pos >> bank_shift
        offset = (pos & (bank_size - 1)) + bank_offset
        # PRG files must have at least 2 bytes (load address lo + hi).
        # Guard against corrupt/truncated entries so we get a clean error
        # instead of an IndexError.
        if entry.length < 2:
            print(
                f"Warning: skipping '{entry.name}' — PRG data is only "
                f"{entry.length} byte(s) (need at least a 2-byte load address).",
                file=sys.stderr,
            )
            dir_pos += _DIR_ENTRY_SIZE
            continue
        size   = entry.length - 2   # strip the 2-byte PRG load address

        # File payload overflow check — done BEFORE writing the directory entry
        # so we never commit a partial/invalid record.
        if pos + size > flash_size:
            sys.exit(
                f'Flash overflow: file "{entry.name}" would push data past the '
                f'{MAX_BANKS}-bank limit ({MAX_BANKS * 0x4000 // 1024} KiB). '
                f'Remove some files or use fewer disk images.'
            )

        # Directory entry: name(16) + bank(2,lo/hi) + offset(2,lo/hi)
        #                + load_addr(2,lo/hi) + size(2,lo/hi)
        name_b = entry.name.encode('latin-1', errors='replace')[:16]
        name_b = name_b.ljust(16, b'\x00')
        flash[dir_pos:          dir_pos + 16] = name_b
        flash[dir_pos + 16]  = bank   & 0xFF
        flash[dir_pos + 17]  = (bank   >> 8) & 0xFF
        flash[dir_pos + 18]  = offset & 0xFF
        flash[dir_pos + 19]  = (offset >> 8) & 0xFF
        flash[dir_pos + 20]  = entry.data[0]   # load address lo
        flash[dir_pos + 21]  = entry.data[1]   # load address hi
        flash[dir_pos + 22]  = size   & 0xFF
        flash[dir_pos + 23]  = (size   >> 8) & 0xFF   # mask: size is 16-bit in flash dir
        dir_pos += _DIR_ENTRY_SIZE

        # File payload (skip the 2-byte load address prefix)
        flash[pos: pos + size] = entry.data[2: 2 + size]
        pos += size

    # 7. Write CHIP packets — one per 8 KB page
    num_pages = (pos + 0x1FFF) >> 13  # round up to 8 KB pages
    max_pages = (flash_size) >> 13    # hard ceiling from flash buffer
    if num_pages > max_pages:
        sys.exit(
            f"Flash overflow: {num_pages} 8 KiB pages needed but EasyFlash "
            f"supports at most {max_pages} ({MAX_BANKS} banks)."
        )

    for page in range(num_pages):
        if bank_size == 0x4000:
            # 16 KB banks: pairs of 8 KB pages share a bank number
            doubled = (first_bank << 1) + page
            bank_hi  = (doubled >> 1) >> 8       # high byte of bank number
            bank_lo  = (doubled >> 1) & 0xFF     # low byte of bank number
            load_hi  = 0x80 + (0x20 if page & 1 else 0x00)  # $8000 or $A000
        else:
            # 8 KB banks
            bn       = first_bank + page
            bank_hi  = bn >> 8
            bank_lo  = bn & 0xFF
            load_hi  = (0x8000 | (bank_offset & 0x2000)) >> 8

        _write_chip_header(out, bank_hi, bank_lo, load_hi)
        out.write(bytes(flash[page << 13: (page << 13) + 0x2000]))

    # 8. Summary
    last_bytes = pos - ((num_pages - 1) << 13) if num_pages else 0
    if bank_size == 0x4000:
        half = '.5' if num_pages & 1 else ''
        print(
            f"disk2easyflash: {num_pages >> 1}{half} banks "
            f"({num_pages * 8} KiB) used   "
            f"({last_bytes} bytes in last 8 KiB page)",
            file=sys.stderr,
        )
    else:
        print(
            f"disk2easyflash: {num_pages} banks "
            f"({num_pages * 8} KiB) used   "
            f"({last_bytes} bytes in last 8 KiB page)",
            file=sys.stderr,
        )


