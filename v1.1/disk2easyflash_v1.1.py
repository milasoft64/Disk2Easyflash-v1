def parse_disk(filename: str) -> List[Entry]:
    """
    Dispatch to the correct parser based on file extension.
    Supports .d64, .d81, and .m2i.
    """
    ext = os.path.splitext(filename)[1].lower()
    if ext == '.d64':
        return parse_d64(filename)
    elif ext == '.d81':
        return parse_d81(filename)
    elif ext == '.m2i':
        return parse_m2i(filename)
    else:
        sys.exit(f"Unsupported disk format: '{ext}'  (use .d64, .d81, or .m2i)")


